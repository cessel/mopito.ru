						<h1>Правила публикации объявлений</h1>						
<p><b>1. Общие положения</b><br><br>
<b>1.1. Допустимая информация в объявлении</b><br>
На Мопито.ру можно размещать объявления о предлагаемых товарах и услугах только в городе, в котором согласно выбранной при регистрации тематике они размещены. Объявления о товарах и услугах, предлагаемых в одном городе, а размещены в другом - не принимаются.<br>
<b>1.2. Достоверность информации в объявлении</b><br>
Информация и фотографии, размещенные в объявлении, должны соответствовать фактической тематике, его характеристиках, местоположению и условиям.<br>
<b>1.3. Один объект — одно объявление</b><br>
Для каждой категории можно создать только одно объявление. Дубликаты объявлений не допускаются.<br>
При создании объявления необходимо указать общие данные о поставщике, его фактическом местонахождении, контактные данные ответственного от поставщика, выбрать соответствующую категорию товаров и услуг, подкатегорию, указать характеристики. Запрещается размещение нескольких объявлений об одних и тех же товарах или услугах с одновременным указанием разных категорий. <br>
Предложение по разным товарам в разных объявлениях в одной категории одного Поставщика недопустимы. В этом случае товары одной категории объединяются в одно объявление. <br><br>
Например, если вы предлагаете в продажу в подкатегории «Мясо и мясная продукция» мясо баранины, а так же в ассортименте имеется мясо птицы, то при регистрации необходимо указать именно эти параметры для одного объявления. Если же по какой либо причине вы не указали какого либо вида продукции, то это можно сделать написав письмо в техподдержку для включения того иного перечня товаров согласно каталогу. <br><br> 
 
<b>2. Блок «Комментарии» при регистрации</b><br><br>
Все разделы объявления должны быть заполнены в соответствии с их названиями и назначением.<br><br>
В блоке «Комментарии» не допускаются:<br>
•	дублирование информации в разных полях;<br>
•	нецензурные выражения;<br>
•	тексты с беспорядочным набором символов и/или без знаков препинания, пробелов;<br>
•	использование только прописных букв в тексте;<br>
•	тексты с большим числом ключевых слов, используемых для продвижения в поисковых системах.<br><br>
 
<b>3. Графа «Из какого вы города» и «Город показа»</b><br><br>
При внесении информации «из какого города компания», необходимо заполнить поля: «из какого вы города» и «… адрес при желании» в указанном городе.   
Необходимо понимать, что «из какого вы города» и «город показа» могут отличаться, так как компания юридически может находиться в любом другом городе, а реализовывать товары и услуги в другом. <br><br>
В графе адрес, необходимо указать адрес (без указания города), и через запятую –номер дома. <br><br>
Запрещается изменять информацию о городе показа и адресе поставщика. Если при заполнении объявления была допущена ошибка, или в процессе публикации объявления  появилась такая необходимость, необходимо обратиться в Техническую службу Мопито.ру.<br><br>
Для размещения информации о других товарах и услугах в других подкатегориях портала, необходимо создать новое объявление.<br><br>
 
<b>4. Стоимость товара и услуг</b><br><br>

Стоимость товаров и услуг в объявлении не указывается и указание о таковом не допускается. <br>
Данная информация оговаривается непосредственно при дальнейшей коммуникации Поставщика и Посетителя страницы вне портала Мопито.ру. <br> <br>

<b>5. Загрузка фото или логотипа</b><br><br>
В случае необходимости или пожелания, фотографии предоставляются Поставщиком после заполнения формы регистрации объявления на портале Мопито.ру через доступные онлайн средства связи при обращении в техническую поддержку.  <br><br>

 
<b>Не допускаются:</b><br>
•	фотографии с рамками, метками, адресами других сайтов и контактной информацией;<br>
•	фотографии, не имеющие отношения к публикуемым товарам и услугам;<br>
•	фотографии низкого качества;<br>
•	коллажи (несколько фотографий в одной).<br><br>
 
Объявление, публикуемое без фото будет иметь картинку по умолчанию от портала Мопито.ру согласно подкатегории ресурса. <br><br>
 
6. <b>Комментарии:</b><br><br>

Мы ввели перечисленные правила, чтобы все пользователи сайта (в т.ч. ваши клиенты) могли легко и быстро найти нужного Поставщика.
В случае многократного нарушения этих правил, ваше объявление может быть <b>заблокировано</b>.
Также хотим отметить, что мы сами исправляем незначительные ошибки в объявлениях, экономя ваше время. Однако если ошибки грубые (либо многочисленные), и мы не можем их исправить самостоятельно, объявление отклоняется, а пользователь получает рекомендации по корректировке через службу технической поддержки.

